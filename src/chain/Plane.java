public class Plane 
{
    private  int fuel;
    private String fuelType;
    private int foodQty;
    private String rampType;
    private String maintenance;
    private String cleaning;

   

    

    /**
     * @return the fuel
     */
    public int getFuel() {
        return fuel;
    }

    /**
     * @param fuel the fuel to set
     */
    public void setFuel(int fuel) {
        this.fuel = fuel;
    }

    /**
     * @return the fuelType
     */
    public String getFuelType() {
        return fuelType;
    }

    /**
     * @param fuelType the fuelType to set
     */
    public void setFuelType(String fuelType) {
        this.fuelType = fuelType;
    }

    /**
     * @return the foodQty
     */
    public int getFoodQty() {
        return foodQty;
    }

    /**
     * @param foodQty the foodQty to set
     */
    public void setFoodQty(int foodQty) {
        this.foodQty = foodQty;
    }

    /**
     * @return the rampType
     */
    public String getRampType() {
        return rampType;
    }

    /**
     * @param rampType the rampType to set
     */
    public void setRampType(String rampType) {
        this.rampType = rampType;
    }

    /**
     * @return the maintenance
     */
    public String getMaintenance() {
        return maintenance;
    }

    /**
     * @param maintenance the maintenance to set
     */
    public void setMaintenance(String maintenance) {
        this.maintenance = maintenance;
    }

    /**
     * @return the cleaning
     */
    public String getCleaning() {
        return cleaning;
    }

    /**
     * @param cleaning the cleaning to set
     */
    public void setCleaning(String cleaning) {
        this.cleaning = cleaning;
    }
    
    
}
